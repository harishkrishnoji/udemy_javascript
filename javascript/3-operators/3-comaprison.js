// comparision operator

let x = 1;

// Relational
console.log(typeof (x > 0));
// response is boolean response

// Relational Operator
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

// Strict Equality Operator
console.log('1' === 1);
// check for type and value

// Lose Equality Operator
console.log(x == 1);
// will not check for type only value
console.log('1' == 1);
console.log('true' == 1);